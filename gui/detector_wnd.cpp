#include "detector_wnd.h"
/////////////////////////////////////////////////////////////////////////////////////
void DetectorWindow::drawAll()
{
    draw::targetMarks(this->_img,
                      _targets,
                      Qt::red);
}
/////////////////////////////////////////////////////////////////////////////////////
void DetectorWindow::inputTargets(ArtifactBox *t)
{
    t->lock().lockForRead();
    _targets = t->data();
    t->lock().unlock();
}
