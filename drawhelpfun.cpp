void drawCross(QImage &img,
               const QPoint &center,
               const int side)
{
    const int side2 = side / 2;
    QPainter painter(&img);
    painter.setPen(QPen(Qt::green, 1));
    painter.drawLine(QPoint(center.x() - side2, center.y() - side2),
                     QPoint(center.x() + side2, center.y() + side2));
    painter.drawLine(QPoint(center.x() + side2, center.y() - side2),
                     QPoint(center.x() - side2, center.y() + side2));
}
