#include "starcat.h"
/////////////////////////////////////////////////////////////////////////////////////
StarCat::StarCat(QSettings *settings) :
    _settings(settings),
    _tvector(new TelescopeVector)
{
    this->moveToThread(this);
    this->start(QThread::NormalPriority);
}
/////////////////////////////////////////////////////////////////////////////////////
StarCat::~StarCat()
{
    this->quit();
    this->terminate();
    delete _tvector;
}
/////////////////////////////////////////////////////////////////////////////////////
void StarCat::telescopeVectorIn(TelescopeVector *tvector,
                                QMutex *mutex)
{
    if(mutex->tryLock(_timeout))
    {
        *_tvector = *tvector;
        mutex->unlock();
        this->processing();
    }
}
