#include "star.h"
/////////////////////////////////////////////////////////////////////////////////////
Star::Star() :
    _alpha(0),
    _delta(0),
    _magnitude(0)
{}
/////////////////////////////////////////////////////////////////////////////////////
Star::Star(const double alpha,
           const double delta,
           const double magn) :
    _alpha(alpha),
    _delta(delta),
    _magnitude(magn)
{}
/////////////////////////////////////////////////////////////////////////////////////
void Star::setAlpha(const double alpha)
{
    _alpha = alpha;
}
/////////////////////////////////////////////////////////////////////////////////////
void Star::setDelta(const double delta)
{
    _delta = delta;
}
/////////////////////////////////////////////////////////////////////////////////////
void Star::setMagnitude(const double magn)
{
    _magnitude = magn;
}
/////////////////////////////////////////////////////////////////////////////////////
double Star::alpha()
{
    return _alpha;
}
/////////////////////////////////////////////////////////////////////////////////////
double Star::delta()
{
    return _delta;
}
/////////////////////////////////////////////////////////////////////////////////////
double Star::magnitude()
{
    return _magnitude;
}
