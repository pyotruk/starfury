#include "artifactbox.h"
/////////////////////////////////////////////////////////////////////////////////////
ArtifactBox::ArtifactBox()
{}
/////////////////////////////////////////////////////////////////////////////////////
ArtifactBox::ArtifactBox(const ArtifactBox &a)
{
    this->_artifacts.resize(a._artifacts.size());
    qCopy(a._artifacts.begin(),
          a._artifacts.end(),
          this->_artifacts.begin());
    this->_timeMarker = a._timeMarker;
}
/////////////////////////////////////////////////////////////////////////////////////
ArtifactBox::~ArtifactBox()
{}
/////////////////////////////////////////////////////////////////////////////////////
ArtifactBox& ArtifactBox::operator =(const ArtifactBox &a)
{
    if(this != &a)
    {
        this->_artifacts.resize(a._artifacts.size());
        qCopy(a._artifacts.begin(),
              a._artifacts.end(),
              this->_artifacts.begin());
        this->_timeMarker = a._timeMarker;
    }
    return *this;
}
/////////////////////////////////////////////////////////////////////////////////////
ArtifactVector& ArtifactBox::artifacts()
{
    return _artifacts;
}
/////////////////////////////////////////////////////////////////////////////////////
QReadWriteLock& ArtifactBox::lock()
{
    return _lock;
}
/////////////////////////////////////////////////////////////////////////////////////
QDateTime& ArtifactBox::timeMarker()
{
    return _timeMarker;
}
