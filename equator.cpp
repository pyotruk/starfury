#include "equator.h"
/////////////////////////////////////////////////////////////////////////////////////
Equator::Equator(QSettings *settings) :
    _settings(settings),
    _artifacts(new ArtifactVector),
    _stars(new ArtifactVector),
    _mutex(new QMutex)
{}
/////////////////////////////////////////////////////////////////////////////////////
Equator::~Equator()
{
    delete _mutex;
    delete _stars;
    delete _artifacts;
}
