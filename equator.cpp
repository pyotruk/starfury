#include "equator.h"
/////////////////////////////////////////////////////////////////////////////////////
Equator::Equator(QSettings *s) :
    _settings(s)
{
    this->moveToThread(this);
    this->start(QThread::NormalPriority);
}
/////////////////////////////////////////////////////////////////////////////////////
Equator::~Equator()
{
    this->quit();
    this->wait();
}
/////////////////////////////////////////////////////////////////////////////////////
void Equator::inputScreenStars(ArtifactBox *a)
{
    a->lock().lockForRead();
    _screenStars = *a;
    a->lock().unlock();

    this->proc(_screenStars, _catStars);
}
/////////////////////////////////////////////////////////////////////////////////////
void Equator::inputCatStars(ArtifactBox *a)
{
    a->lock().lockForRead();
    _catStars = *a;
    a->lock().unlock();
}
