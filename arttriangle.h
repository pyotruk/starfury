#ifndef ARTTRIANGLE_H
#define ARTTRIANGLE_H
/////////////////////////////////////////////////////////////////////////////////////
/* Класс для решения задачи отождествления звёздных конфигураций
   методом подобных треугольников.
   Для экономии памяти класс работает с указателями
   (постарайся не выстрелить себе в ногу). */
/////////////////////////////////////////////////////////////////////////////////////
#include <QVector>
#include <QPoint>
/////////////////////////////////////////////////////////////////////////////////////
#include "artifact.h"
#include "astrocalc.h"
#include "qmath.h"
/////////////////////////////////////////////////////////////////////////////////////
class ArtifactTriangle
{
public:
    ArtifactTriangle(Artifact *a = 0,
                     Artifact *b = 0,
                     Artifact *c = 0);
    bool isValid();
    bool isEqualTo(const ArtifactTriangle&,
                   const double eps);
    bool isSimilarTo(const ArtifactTriangle&,
                     const double eps);
private:
    Artifact *_a;
    Artifact *_b;
    Artifact *_c;
};
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
typedef QVector<ArtifactTriangle> ArtifactTriangleVector;
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
#endif // ARTTRIANGLE_H
