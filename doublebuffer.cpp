#include "doublebuffer.h"
/////////////////////////////////////////////////////////////////////////////////////
DoubleBuffer::DoubleBuffer() :
    _rapidBuf(new Frame),
    _slowBuf(new Frame)
{ }
/////////////////////////////////////////////////////////////////////////////////////
DoubleBuffer::~DoubleBuffer()
{
    delete _rapidBuf;
    delete _slowBuf;
}
/////////////////////////////////////////////////////////////////////////////////////
void DoubleBuffer::switchBuffers()
{
    Frame *foo = _rapidBuf;
    _rapidBuf = _slowBuf;
    _slowBuf = foo;
}
/////////////////////////////////////////////////////////////////////////////////////
Frame *DoubleBuffer::rapidBuf()
{
    return _rapidBuf;
}
/////////////////////////////////////////////////////////////////////////////////////
Frame *DoubleBuffer::slowBuf()
{
    return _slowBuf;
}
/////////////////////////////////////////////////////////////////////////////////////
void DoubleBuffer::unlockSlowBuf()
{
    _slowBuf->unlock();
}
/////////////////////////////////////////////////////////////////////////////////////
