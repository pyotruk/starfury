#include "frameproc.h"
/////////////////////////////////////////////////////////////////////////////////////
void FrameProc::FrameProcSlot(void *pFrame,
                              int frameWidth,
                              int frameHeight)
{
    cv::Mat cvMat(frameHeight, frameWidth, CV_8UC1);
    cvMat.data = (uchar*)pFrame;
}

