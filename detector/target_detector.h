#ifndef TARGET_DETECTOR_H
#define TARGET_DETECTOR_H
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
#include "detector/abstract_detector.h"
/////////////////////////////////////////////////////////////////////////////////////
class TargetDetector : public AbstractDetector
{
public:
    explicit TargetDetector(QSettings *s) : AbstractDetector(s) {}
protected:
    virtual void accumulate()
    {
        cvwrap::medianBlur(_frame.data().asCvMat(), _blurKernelSize);
        _frame = _accum.add(_frame);
    }
};
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
#endif // TARGET_DETECTOR_H
