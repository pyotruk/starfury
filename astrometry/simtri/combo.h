#ifndef COMBO_H
#define COMBO_H
/////////////////////////////////////////////////////////////////////////////////////
/* Класс для хранения комбинаций перебора точек
   при формировании массива треугольников
   (астрометрия) */
/////////////////////////////////////////////////////////////////////////////////////
#include <QVector>
/////////////////////////////////////////////////////////////////////////////////////
typedef int RawCombo[3];
/////////////////////////////////////////////////////////////////////////////////////
class Combo
{
public:
    explicit Combo(int i = 0,
                   int j = 0,
                   int k = 0);
    bool operator ==(const Combo&) const;
private:
    RawCombo _raw;
};
/////////////////////////////////////////////////////////////////////////////////////
typedef QVector<Combo> ComboVector;
/////////////////////////////////////////////////////////////////////////////////////
namespace comb
{
bool isContain(const ComboVector&,
               const Combo&);
}
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
#endif // COMBO_H
